
package Model.EtiquetasModel;


public class EtiquetaLote extends Etiqueta{
    
    
    private int noEtiquetaLote;
    private int numEtiqueta;
    private int cantidad;

    public int getNoEtiquetaLote() {
        return noEtiquetaLote;
    }

    public void setNoEtiquetaLote(int noEtiquetaLote) {
        this.noEtiquetaLote = noEtiquetaLote;
    }

    public int getNumEtiqueta() {
        return numEtiqueta;
    }

    public void setNumEtiqueta(int numEtiqueta) {
        this.numEtiqueta = numEtiqueta;
    }

    public int getCantidad() {
        return cantidad;
    }

    public void setCantidad(int cantidad) {
        this.cantidad = cantidad;
    }
    
}
