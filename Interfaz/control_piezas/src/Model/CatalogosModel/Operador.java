
package Model.CatalogosModel;


public class Operador {
    
    private int noOperador;
    private String codOperador;
    private String nombreOperador;

    public Operador() {
    
    }

    public int getNoOperador() {
        return noOperador;
    }

    public void setNoOperador(int noOperador) {
        this.noOperador = noOperador;
    }

    public String getCodOperador() {
        return codOperador;
    }

    public void setCodOperador(String codOperador) {
        this.codOperador = codOperador;
    }

    

    public String getNombreOperador() {
        return nombreOperador;
    }

    public void setNombreOperador(String nombreOperador) {
        this.nombreOperador = nombreOperador;
    }
    
}
