
package Model.CatalogosModel;


public class Contacto extends Cliente{
    
    
    private int noContacto;
    private String descContacto;
    private String descDepartamento;
    private String telefono;
    private String extencion;
    private String celular;
    private String correo;

    public Contacto() {
    }

    public int getNoContacto() {
        return noContacto;
    }

    public void setNoContacto(int noContacto) {
        this.noContacto = noContacto;
    }

    public String getDescContacto() {
        return descContacto;
    }

    public void setDescContacto(String descContacto) {
        this.descContacto = descContacto;
    }

    public String getDescDepartamento() {
        return descDepartamento;
    }

    public void setDescDepartamento(String descDepartamento) {
        this.descDepartamento = descDepartamento;
    }

    public String getTelefono() {
        return telefono;
    }

    public void setTelefono(String telefono) {
        this.telefono = telefono;
    }

    public String getExtencion() {
        return extencion;
    }

    public void setExtencion(String extencion) {
        this.extencion = extencion;
    }

    public String getCelular() {
        return celular;
    }

    public void setCelular(String celular) {
        this.celular = celular;
    }

    public String getCorreo() {
        return correo;
    }

    public void setCorreo(String correo) {
        this.correo = correo;
    }
    
    
    
    
    
}
